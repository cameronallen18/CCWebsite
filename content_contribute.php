<p class="noindent">Fund Description:</p>
<p class="noindent">The Chocolate City Academic Incentive  Fund is an MIT endowed account where Chocolate City alumni (and  others) can pledge to donate directly to Chocolate City.  The fund  will be administered by the Director of the Office of Minority  Education, who is currently Karl Reid (Chocolate City Class of 1984).  All monies contributed to the Chocolate City Academic Incentive Fund  will be held as part of the MIT endowment. The yearly interest  accumulated (3 to 4%) will be released to Chocolate City if the  house&rsquo;s GPA for both the Fall and Spring semesters reaches or  surpasses a specific target set annually by the Director of the  Office of Minority Education in coordination with a committee of  Chocolate City alumni and current undergraduate students. The  Housemaster, the Graduate Resident Tutor, the CC co-chairs, and the  CC Academic Chair will meet each summer to certify the house grade  point average and approve the release of funds. Additional available  income thereafter will be returned to principal. </p>
