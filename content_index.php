<h5 class="center">Welcome to Chocolate City @ MIT</h5>

<p>Welcome to the Chocolate City @ MIT website!&nbsp; I hope that you find your way around the  website easily!&nbsp; When you first hear the  words Chocolate City, many probably believe that we are group who loves  Chocolate!&nbsp; We first want to let you know  that we are more than that.&nbsp; Chocolate  City @ MIT is a living group currently living on the top 3 floors of New House  1 dormitory.&nbsp; It was established in 1975,  with the mission of creating a support network for all of the brothers  within.&nbsp; Our current major goals are to  develop each of the individual brothers intellectually, professionally, as well  as socially.</p>
<p>We achieve these goals by doing many major events.&nbsp; We develop the brothers professionally and  intellectually by doing the 4-year planning seminar, Faculty Reception, as well  as holding study sessions within the house.&nbsp;  The 4-year planning seminar helps freshman and sophomores at MIT plan  out their 4 years in college with each of their respective majors.&nbsp; Chocolate City&rsquo;s Faculty Reception is an  opportunity for the Brothers of Chocolate City as well as anybody else who  wishes to attend to be able to interact with the faculty and staff at MIT.&nbsp; Study sessions are held before most of the  freshman exams to ensure that they are well prepared for their exam so that  they get off on the right foot.</p>
<p>Chocolate City also strives to build the social networks of  every brother in the house.&nbsp; At the  beginning of every semester, we host a huge party on the campus of MIT.&nbsp; Our previous parties have had 650+  attendees!&nbsp; They have always been a huge  success and also help fund the other events that Chocolate City has throughout  the school year, such as our semi-annual retreat where we have the opportunity  to reevaluate the direction the house is going in as well as strengthen the  brotherhood.&nbsp; Chocolate City is also open  to having social networking events with other organizations within the Boston  community.&nbsp; These events have served as  amazing opportunities for the brothers to increase their network with the rest  of the Boston community.&nbsp; If you are  interested in having a social with the Brothers of Chocolate City, please be  sure to contact our Social Chairs listed on the officer page.</p>
<p>Chocolate City has long served the Boston collegiate  community and hopes to continue doing so.&nbsp;  Please be on the lookout for some of our upcoming events which include  Comedy Collage (2/20/2010) as well as the 35th Anniversary (October  2010).&nbsp; We hope that you will take the  time to look through this webpage and please don&rsquo;t be afraid to contact any of  us via email if you are to have any questions.</p>
<p class="noindent">Sincerely,</p>
